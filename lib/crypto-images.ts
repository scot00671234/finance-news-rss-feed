export function getRandomCryptoImage(category: string, title?: string): string {
  const images = CRYPTO_FALLBACK_IMAGES[category as keyof typeof CRYPTO_FALLBACK_IMAGES] || CRYPTO_FALLBACK_IMAGES.default
  
  // Use title hash for deterministic but varied selection
  if (title) {
    let hash = 0
    for (let i = 0; i < title.length; i++) {
      const char = title.charCodeAt(i)
      hash = ((hash << 5) - hash) + char
      hash = hash & hash // Convert to 32-bit integer
    }
    const index = Math.abs(hash) % images.length
    return images[index]
  }
  
  // Fallback to random if no title
  const randomIndex = Math.floor(Math.random() * images.length)
  return images[randomIndex]
}

export function getCryptoImageByIndex(category: string, index: number): string {
  const images = CRYPTO_FALLBACK_IMAGES[category as keyof typeof CRYPTO_FALLBACK_IMAGES] || CRYPTO_FALLBACK_IMAGES.default
  return images[index % images.length]
}
